// 检测是否在 Vercel 环境
$is_vercel = isset($_ENV['VERCEL']) || isset($_SERVER['VERCEL']);

return [
    // 默认缓存驱动 - 统一使用文件缓存
    'default' => 'file',

    // 缓存连接方式配置
    'stores'  => [
        'file' => [
            // 驱动方式
            'type'       => 'File',
            // 缓存保存目录 - Vercel 环境使用 /tmp，本地使用默认
            'path'       => $is_vercel ? '/tmp/cache' : '',
            // 缓存前缀
            'prefix'     => '',
            // 缓存有效期 0表示永久缓存
            'expire'     => 0,
            // 缓存标签前缀
            'tag_prefix' => 'tag:',
            // 序列化机制 例如 ['serialize', 'unserialize']
            'serialize'  => [],
        ],
    ],
];
